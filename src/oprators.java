public class oprators {
    public static void main(String[] args) {
        // these are + - * / % OP's.

        int a  =  20;
        int b = 40;

        //sums
        int sum = a + b;
        // products
        int product =  a * b ;

        //relational OP's
        boolean isEqualTo = (a == b);

        //print to console
        System.out.println("Sum: " + sum);
        System.out.println("Product: " + product);
        System.out.println("Is Equal: " + isEqualTo);
    }
}
